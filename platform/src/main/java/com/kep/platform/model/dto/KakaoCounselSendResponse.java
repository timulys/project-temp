package com.kep.platform.model.dto;

import lombok.*;
import org.hibernate.validator.constraints.URL;

import javax.validation.constraints.NotEmpty;
import javax.validation.constraints.NotNull;
import java.util.List;

/**
 * '카카오 상담톡 발신 이벤트' 응답
 */
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class KakaoCounselSendResponse {

	@NotNull
	private Integer code;

	@NotEmpty
	private String message;

	// ////////////////////////////////////////////////////////////////////////
	// Message Event Only
	// ////////////////////////////////////////////////////////////////////////
	@NotEmpty
	private String createdAt;

	// ////////////////////////////////////////////////////////////////////////
	// Upload Image Event Only
	// ////////////////////////////////////////////////////////////////////////
	@NotEmpty
	@URL
	private String image;

	// ////////////////////////////////////////////////////////////////////////
	// Upload File Event Only
	// ////////////////////////////////////////////////////////////////////////
	@NotEmpty
	@URL
	private String file;
	@NotEmpty
	private String name;
	@NotEmpty
	private Long size;

	// ////////////////////////////////////////////////////////////////////////
	// End Event Only
	// ////////////////////////////////////////////////////////////////////////
	/**
	 * 종료된 세션 아이디
	 */
	@NotEmpty
	private String sessionId;

	// ////////////////////////////////////////////////////////////////////////
	// Bot Event Only
	// ////////////////////////////////////////////////////////////////////////
	/**
	 * 봇 대화 이력
	 */
	private List<KakaoCounselReceiveRelayBotEvent> chatbotMessages;
}
